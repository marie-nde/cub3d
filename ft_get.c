#include "cub3d.h"

int		ft_get_resol(char *str, char c)
{
	int i;

	i = 2;
	if (c == 'y')
	{
		while (str && str[i] && str[i] >= '0' && str[i] <= '9')
			i++;
		i++;
	}
	return (ft_atoi(str + i));
}
